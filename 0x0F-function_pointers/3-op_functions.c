#include "3-calc.h"

/**
 * op_add - finds sum
 * @a: first number
 * @b: second
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - finds a - b
 * @a: first num
 * @b: second num
 * Return: a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies a & b
 * @a: 1st num
 * @b: 2nd num
 * Return: a * b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - finds quotient
 * @a: 1st num
 * @b: 2nd num
 * Return: a / b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - finds remainder(a % b)
 * @a: 1st num
 * @b: 2nd num
 * Return: Remainder = (a % b)
 */
int op_mod(int a, int b)
{
	return (a % b);
}
